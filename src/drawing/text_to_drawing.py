def _variant_caution_line(variant: int) -> list[tuple[float, float]]:
    x, y = _variant_point(variant, "caution")
    if variant % 2 == 0:
        return [(x, y - 0.027), (x, y + 0.02)]
    return [(x - 0.023, y), (x + 0.023, y)]


def _variant_extra_marks(variant: int, score: int) -> list[Stroke]:
    if variant == 0:
        return _sun_rays(center=(0.0, 0.12), radius=0.018 + 0.00008 * score)
    if variant == 1:
        return [
            _diamond_points(center=(-0.012, 0.112), radius=0.025),
            _angled_arc_points(center=(-0.004, 0.116), radius=0.024, start_angle=1.55, end_angle=4.7),
        ]
    if variant == 2:
        return [
            triangle_points(center=(0.02, 0.118), side=0.052, rotation=math.pi / 2.0),
            polyline_points([(-0.006, 0.108), (0.02, 0.138), (0.046, 0.108)]),
        ]
    return [
        _zigzag_wave_points(start=(-0.036, 0.124), width=0.082, amplitude=0.008, cycles=1.3),
        polyline_points([(-0.042, 0.1), (0.0, 0.137), (0.044, 0.1)]),
    ]


def _interpolate_strokes(strokes: list[np.ndarray], spacing: float) -> np.ndarray:
    from .shapes import interpolate_polyline

    if not strokes:
        return np.zeros((0, 3), dtype=float)
    return interpolate_polyline(np.vstack(strokes), spacing=spacing)


def _stable_seed(text: str) -> int:
    digest = hashlib.sha256(text.encode("utf-8")).hexdigest()
    return int(digest[:8], 16)


def _has_any(text: str, needles: list[str]) -> bool:
    return any(needle in text for needle in needles)
